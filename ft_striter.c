#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int run;

	if ((s == NULL) || (f == NULL))
		return ;
	run = 0;
	while (s[run])
	{
		f(&s[run]);
		run++;
	}
}
